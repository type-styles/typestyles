<script setup lang="ts">
import { ref } from 'vue';
import { button } from './styles';

const ghost = ref(false);
</script>

<template>
  <main class="page">
    <h1>typestyles + Vue</h1>
    <p>Typed component styles with ordinary <code>class</code> bindings.</p>
    <button
      type="button"
      :class="button({ ghost: ghost })"
      @click="ghost = !ghost"
    >
      {{ ghost ? 'Ghost' : 'Primary' }}
    </button>
  </main>
</template>

